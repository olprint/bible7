const char* chap561 = "A Psalm of Canticle to the sons of Core, the second of the Sabbath.\n"
	"Great is our Lord, and to be praised exceedingly, in the city of our God, in his holy mount.\n"
	"Mount Sion is founded with the exultation of the whole earth, the sides of the North, the city of the great King.\n"
	"God shall be known in the houses thereof, when he shall receive it.\n"
	"For behold the kings of the earth were gathered together: they assembled in one.\n"
	"They seeing it so were in admiration, were troubled, were moved:\n"
	"Trembling took them. Their sorrows as a woman travailing.\n"
	"In a vehement spirit thou shalt break the ships of Tharsis.\n"
	"As we have heard, so have we seen in the city of the Lord of hosts in the city of our God: God hath founded it for ever.\n"
	"We have received thy mercy, o God, in the midst of thy temple.\n"
	"According to thy name, o God, so also is thy praise unto the ends of the earth: thy right hand is full of justice.\n"
	"Let mount Sion be glad, and the daughters of Juda rejoice, because of thy judgments, o Lord.\n"
	"Compass Sion, and embrace ye her: tell ye in her towers.\n"
	"Set your hearts on her strength: and distribute ye her houses, that you may declare it in another generation.\n"
	"Because this is God, our God for ever, and for ever and ever: he shall rule us evermore.";